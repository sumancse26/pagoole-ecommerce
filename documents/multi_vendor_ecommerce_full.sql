CREATE OR REPLACE PROCEDURE discount_shop.PROC_SET_DEFAULT_VALUE
IS
    vParentId   NUMBER;
    vChuncId    NUMBER := TO_NUMBER (TO_CHAR (SYSDATE, 'YYYYMMDDHH24MISS'));
BEGIN
