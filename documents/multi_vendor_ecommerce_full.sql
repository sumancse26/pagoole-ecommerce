        UNION ALL
        SELECT 3, 0.5, 'kg', '500 grams' FROM DUAL
        UNION ALL
        SELECT 4, 1.0, 'kg', '1 kilogram' FROM DUAL
        UNION ALL
        SELECT 5, 2.0, 'kg', '2 kilograms' FROM DUAL
        UNION ALL
        SELECT 6, 3.0, 'kg', '3 kilograms' FROM DUAL
        UNION ALL
        SELECT 7, 4.0, 'kg', '4 kilograms' FROM DUAL
        UNION ALL
        SELECT 8, 5.0, 'kg', '5 kilograms' FROM DUAL;

