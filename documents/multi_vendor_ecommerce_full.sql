CREATE OR REPLACE PROCEDURE discount_shop.ALL_INS_ERROR_LOG (
    p_code      NUMBER,
    p_message   VARCHAR2,
    p_app_id    NUMBER DEFAULT -1,
    p_page_id   NUMBER DEFAULT -1,
    p_user      VARCHAR2 DEFAULT USER)
IS
    PRAGMA AUTONOMOUS_TRANSACTION;
    l_username   VARCHAR2 (64);
BEGIN
    BEGIN
        IF p_user IS NULL
        THEN
            l_username := SYS_CONTEXT ('APEX$SESSION', 'APP_USER');
        ELSE
            l_username := p_user;
        END IF;
    EXCEPTION
        WHEN OTHERS
        THEN
            l_username := 'Auto';
    END;

    INSERT INTO discount_shop.ALL_ERROR_LOG (ERROR_CODE,
                                             ERROR_MSG,
                                             APP_ID,
                                             PAGE_ID,
                                             USERNAME)
         VALUES (p_code,
                 SUBSTR (p_message, 1, 4000),
                 NVL (SYS_CONTEXT ('APEX$SESSION', 'APP_ID'), p_app_id),
                 p_page_id,
                 NVL (SYS_CONTEXT ('APEX$SESSION', 'APP_USER'), l_username));

    COMMIT;
EXCEPTION
    WHEN OTHERS
    THEN
        raise_application_error (
            -20001,
               'An error was encountered - Procedure Name: '
            || $$plsql_unit
            || ' at Line No : '
            || $$plsql_line
            || ' Error Code : '
            || SQLCODE
            || ' -ERROR- '
            || SQLERRM);
END ALL_INS_ERROR_LOG;

INSERT INTO discount_shop.ALL_OBJECT_HISTORY (ID,
                                              NAME,
                                              DESCRIPTION,
                                              ORDER_BY)
    SELECT NVL (MAX (ID), 0) + 1,
           'discount_shop.ALL_INS_ERROR_LOG',
           'Insert error log data',
           NVL (MAX (ORDER_BY), 0) + 1
      FROM discount_shop.ALL_OBJECT_HISTORY;

COMMIT;
/

CREATE OR REPLACE PROCEDURE discount_shop.ALL_INS_PROCESS_LOG (
    p_process_serial   IN OUT NUMBER,
    p_chunk_id                NUMBER,
    p_message                 VARCHAR2)
IS
    PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
    IF p_process_serial IS NULL
    THEN
        INSERT INTO discount_shop.ALL_PROCESS_LOG (CHUNK_ID, MESSAGE)
                 VALUES (
                            NVL (p_chunk_id,
                                 TO_CHAR (SYSDATE, 'YYMMDDHH24MISS')),
                            p_message);

        p_process_serial := NULL;
    ELSE
        INSERT INTO discount_shop.ALL_PROCESS_LOG (ID, CHUNK_ID, MESSAGE)
             VALUES (p_process_serial, p_chunk_id, p_message);

        p_process_serial := p_process_serial + 1;
    END IF;

    COMMIT;
EXCEPTION
    WHEN OTHERS
    THEN
        discount_shop.ALL_INS_ERROR_LOG (
            p_code      => SQLCODE,
            p_message   =>
                   'An error was encountered - Procedure Name: '
                || $$plsql_unit
                || ' at Line No : '
                || $$plsql_line
                || ' Error Code : '
                || SQLCODE
                || ' -ERROR- '
                || SQLERRM,
            p_app_id    => -2,
            p_page_id   => -2,
            p_user      => USER);
END ALL_INS_PROCESS_LOG;


INSERT INTO discount_shop.ALL_OBJECT_HISTORY (ID,
                                              NAME,
                                              DESCRIPTION,
                                              ORDER_BY)
    SELECT NVL (MAX (ID), 0) + 1,
           'discount_shop.PROC_PROCESS_LOG',
           'Insert process log data',
           NVL (MAX (ORDER_BY), 0) + 1
      FROM discount_shop.ALL_OBJECT_HISTORY;

COMMIT;
