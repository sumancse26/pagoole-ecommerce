        p_process_serial   => vChuncId,
        p_chunk_id         => vChuncId,
        p_message          => 'Inserted 1 user as default User with id 0.');

    -- Step 2: Delete existing data
    DELETE FROM discount_shop.WEIGHTS;

    -- Step 3: Insert data with manual ID values
    INSERT INTO discount_shop.WEIGHTS (ID,
                                       VALUE,
                                       UNIT,
                                       DESCRIPTION)
        SELECT 1, 0.1, 'kg', '100 grams' FROM DUAL
        UNION ALL
